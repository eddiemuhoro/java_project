package com.example.students;

public class Stude {
     String name;
     String course;
     String regNo;

    public Stude(String name, String course, String regNo) {
        this.name = name;
        this.course = course;
        this.regNo = regNo;
    }

    public String getName() {
        return name;
    }



    public String getCourse() {
        return course;
    }



    public String getRegNo() {
        return regNo;
    }


}
